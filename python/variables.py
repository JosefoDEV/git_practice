# Variables can store a data that we need to use later. 
# variables can use only _ in their  names
# Example:
# my_special_variable = 45
# They cannot begin with numbers, but they can contain numbers after first letter (e.g., var_5 = 5)


message = "Help me"

# prints "Help me"
print(message)


# we can update the variable later as we want

message = "Hello"

print(message)